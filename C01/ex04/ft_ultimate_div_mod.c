void	ft_ultimate_div_mod(int *a, int *b)
{
	int	temp;
	int	temp2;

	if (*b != 0)
	{
		temp = *a;
		temp2 = *b;
		*b = temp % temp2;
		*a = temp / temp2;
	}
}
