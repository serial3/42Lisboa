void	ft_ultimate_div_mod(int *a, int *b)
{
	int	div;
	int	reminder;

	if (a && b && *b != 0)
	{
		div = *a / *b;
		reminder = *a % *b;
		*a = div;
		*b = reminder;
	}
}
