void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	helper;

	i = 0;
	while (i < size / 2)
	{
		helper = *(tab + i);
		*(tab + i) = *(tab + (size - i - 1));
		*(tab + (size - i - 1)) = helper;
		i++;
	}
}
